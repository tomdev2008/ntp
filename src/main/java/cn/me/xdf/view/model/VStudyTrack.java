package cn.me.xdf.view.model;

public class VStudyTrack {


	/**
	 * 用户姓名
	 */
	private String userName;
	
	/**
	 * 用户部门
	 */
	private String userDep;
	
	/**
	 * 用户电话
	 */
	private String userTel;
	
	/**
	 * 用户email
	 */
	private String userEmai;
	
	/**
	 * 课程名
	 */
	private String courseName;
	
	/**
	 * 导师姓名
	 */
	private String guideTeachName;
	
	/**
	 * 当前环节
	 */
	private String linkNow;

	/**
	 * 学习情况
	 */
	private String studyInofNow;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserDep() {
		return userDep;
	}

	public void setUserDep(String userDep) {
		this.userDep = userDep;
	}

	public String getUserTel() {
		return userTel;
	}

	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}

	public String getUserEmai() {
		return userEmai;
	}

	public void setUserEmai(String userEmai) {
		this.userEmai = userEmai;
	}

	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

	public String getGuideTeachName() {
		return guideTeachName;
	}

	public void setGuideTeachName(String guideTeachName) {
		this.guideTeachName = guideTeachName;
	}

	public String getLinkNow() {
		return linkNow;
	}

	public void setLinkNow(String linkNow) {
		this.linkNow = linkNow;
	}

	public String getStudyInofNow() {
		return studyInofNow;
	}

	public void setStudyInofNow(String studyInofNow) {
		this.studyInofNow = studyInofNow;
	}
	

	

}
